    private void bT1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bT1ActionPerformed
        if (RBCelcius1.isSelected() && RBKelvin2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaCK(a)));
        } else if (RBCelcius1.isSelected() && RBFahrenheit2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaCF(a)));
        } else if (RBKelvin1.isSelected() && RBCelcius2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaKC(a)));
        } else if (RBKelvin1.isSelected() && RBFahrenheit2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaKF(a)));
        } else if (RBFahrenheit1.isSelected() && RBCelcius2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaFC(a)));
        } else if (RBFahrenheit1.isSelected() && RBKelvin2.isSelected()) {
            int a = Integer.parseInt(TF1.getText().trim());

            TF2.setText(String.valueOf(conversor.calculaFK(a)));
        } else {
            TF2.setText(TF1.getText());
        }
    }//GEN-LAST:event_bT1ActionPerformed

